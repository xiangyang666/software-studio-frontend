<script setup>
import { onMounted } from "vue";

onMounted(() => {
  //侧边栏
  var show = document.querySelectorAll(".show .lis");
  var category = document.querySelectorAll(".show .lis .children");

  //遍历所有的show
  for (var i = 0; i < show.length; i++) {
    show[i].index = i;
    show[i].onmouseover = function (e) {
      for (var j = 0; j < show.length; j++) {
        show[j].style.backgroundColor = "";
      }
      //移到哪个那个添加背景颜色
      this.style.backgroundColor = "#ff6700";
      for (var z = 0; z < category.length; z++) {
        category[this.index].style.display = "block";
      }
    };
    show[i].onmouseout = function () {
      for (var j = 0; j < show.length; j++) {
        show[j].style.backgroundColor = "";
      }
      for (var z = 0; z < category.length; z++) {
        category[z].style.display = "none";
      }
    };
  }

  //轮播图
  var prev = document.querySelector(".prev");
  var next = document.querySelector(".next");
  var list = document.querySelectorAll(".list li");
  var point = document.querySelectorAll(".point ul li");

  var index = 0;
  //封装函数
  function changeOne(type) {
    list[index].classList.remove("active");
    point[index].classList.remove("pot");

    if (type === true) {
      index++;
    } else if (type === false) {
      index--;
    } else {
      index = type;
    }

    if (index >= point.length) {
      index = 0;
    }
    if (index < 0) {
      index = point.length - 1;
    }

    list[index].classList.add("active");
    point[index].classList.add("pot");
  }

  var carousel = document.querySelector(".carousel");

  carousel.onclick = function (e) {
    var e = event.event || window.event;

    if (e.target.className === "next") {
      changeOne(true);
    } else if (e.target.className === "prev") {
      changeOne(false);
    } else if (e.target.dataset.name === "tab") {
      var i = e.target.dataset.i - 1;
      changeOne(i);
    }
  };

  setInterval(function () {
    changeOne(true);
  }, 5000);
});
</script>

